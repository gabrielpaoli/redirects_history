  function redirectType($url, $urlsWithVariables, $redirectTo302, $exceptions, $countryInUrl){

    $url = removeHtpps($url);
    $url = rtrim($url, "/");

    $urlInParts = explode('/', $url);
    
    $urlParsed = convertUrlException($url, $countryInUrl, $countries);
    if(in_array($urlParsed, $exceptions)):

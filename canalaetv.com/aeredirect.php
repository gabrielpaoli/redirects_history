  function parseUrl($url, $countryInUrl, $variableInUrl, $oldUrl, $newUrl, $exceptions){

    //Si es una excepcion

    if(isPlayUrl($url)):
      //OJO, LA URL VIENE SIN EL PAIS.
      $url = existParsedUrlInArrayOldUrl($url, $oldUrl);
      $url = matchNewUrl($url, $oldUrl, $newUrl);

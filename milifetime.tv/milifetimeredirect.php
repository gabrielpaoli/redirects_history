    foreach($mergeOldUrlAndNewUrl as $mergeOldUrlAndNewUrlInn):
      if($url == $mergeOldUrlAndNewUrlInn['oldUrl']):
        return $mergeOldUrlAndNewUrlInn['newUrl'];
      endif;
    endforeach;
  }

